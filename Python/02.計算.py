a = 10
b = 5

print('コンピュータさん：私は計算ができます。')
print()

print(a, '＋', b, '=', a + b)
print(a, '－', b, '=', a - b)
print(a, '×', b, '=', a * b)
print(a, '÷', b, '=', a / b, ' 余り', a % b)
print()

#　■要点■
#　●掛け算には『*』割り算には『/』を使う。
#　●余りには『%』を使う。
#　●変数は何らかの値を入れておける入れもの。
#　●変数へは『=』を使って値を入れることができる。
#　●変数名を書けば変数に入っている値の代わりになる。